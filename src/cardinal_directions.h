/**
 * @file cardinal_directions.h
 * @brief This file defines the CardinalDirection enum, representing the four cardinal directions.
 */
#ifndef CARDINAL_DIRECTIONS_H
#define CARDINAL_DIRECTIONS_H

/**
 * @brief Enum representing the four cardinal directions.
 */
typedef enum
{
    CARDINAL_DIRECTION_NORTH = 0, ///< North direction
    CARDINAL_DIRECTION_EAST = 1,  ///< East direction
    CARDINAL_DIRECTION_SOUTH = 2, ///< South direction
    CARDINAL_DIRECTION_WEST = 3  ///< West direction
} CardinalDirection;

#endif
